#relational operators
# < > == !=
a=50
b=100
print(a>b)
print(a<b)
print(a==b)
print(a!=b)

#logical operator
# & |
a= True
b= False
print(a & a) #true & true = true
print(b & a) #false & true = false
print(b & b) #false & false = false
print(a|a) #true & true = true
print(a|b) #true & false = true
print(b|a) #false & true = true
print(b|a) #false & true = true
print(b|b) #false & false = false