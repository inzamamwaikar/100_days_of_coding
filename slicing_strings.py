my_str="Subhash_Vayugundala"
print(my_str)

#slicing can be possible using 2 indexes i.e. start_index(inclusive) and end_index(exclusive)
#indexing starting from in python 3
print(my_str[2:5])

#if we give start index as blank then it will take 0
print(my_str[:5])

#if we give end index as  blank then it will take till end (upto last char of string)
print(my_str[2:])

print(my_str[7:15])
print(my_str[4:8])
print(my_str[15:])
print(my_str[15:18])

my_str="Subhash_Vayugundala"
print(my_str)

print(my_str[-5:-2])
print(my_str[-12:-1])
print(my_str[-16:-6])
print(my_str[3:-6]) # in this case both start and end index are inclusive
print(my_str[1:12])
print(my_str[1:-7])
